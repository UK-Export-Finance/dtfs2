const express = require('express');
const { companiesHouse, validateCompaniesHouse } = require('../controllers/companies-house');
const { validateRole, validateToken, validateBank } = require('../middleware');
const { MAKER } = require('../constants/roles');

const router = express.Router();

/**
 * @openapi
 * /application-details/:dealId/companies-house:
 *   get:
 *     summary: Get the companies house page.
 *     tags: [Portal - Gef]
 *     description: Get the companies house page.
 *     parameters:
 *       - in: path
 *         name: dealId
 *         schema:
 *           type: string
 *         required: true
 *         description: the deal ID
 *     responses:
 *       200:
 *         description: OK
 *       400:
 *         description: Bad request
 *       401:
 *         description: Unauthorised insertion
 *       403:
 *         description: Forbidden
 *       404:
 *         description: Not found
 *       500:
 *         description: Internal server error
 *   post:
 *     summary: Validates companies house page input and updates the application with the retrieved company details.
 *     tags: [Portal - Gef]
 *     description: Validates companies house page input and updates the application with the retrieved company details.
 *     parameters:
 *       - in: path
 *         name: dealId
 *         schema:
 *           type: string
 *         required: true
 *         description: the deal ID
 *     requestBody:
 *       required: true
 *       content:
 *         application/json:
 *           schema:
 *             type: object
 *     responses:
 *       200:
 *         description: OK
 *       301:
 *         description: Resource moved permanently
 *       400:
 *         description: Bad request
 *       401:
 *         description: Unauthorised insertion
 *       403:
 *         description: Forbidden
 *       404:
 *         description: Not found
 *       500:
 *         description: Internal server error
 */
router
  .route('/application-details/:dealId/companies-house')
  .all([validateToken, validateBank, validateRole({ role: [MAKER] })])
  .get(companiesHouse)
  .post(validateCompaniesHouse);

module.exports = router;
